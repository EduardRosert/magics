C
C     PLOT TITLE
C
      CALL PSETC ('TEXT_CHARACTER_1','Cylindrical Map Projection ')
      CALL PSETC ('TEXT_CHARACTER_2',
     1            'Default values for MAGICS Parameters Used')
C
      CALL PSETC ('TEXT_CHARACTER_3',
     1            'Area is Whole Globe, Defined by Coordinates of'//
     2            ' LOWER_LEFT and UPPER_RIGHT Corners ')

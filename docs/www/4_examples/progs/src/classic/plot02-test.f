      CALL PTEXT
C
      RETURN
      END
      SUBROUTINE PLOT02_TWO
C
C     PLOT A SELECTED AREA DEFINED BY COORDINATES OF LOWER_LEFT AND
C     UPPER_RIGHT CORNERS.  PROJECTION IS CYLINDRICAL.  A TEXT BLOCK
C     IS PLOTTED ON A BLANKED AREA.
C
C     ALL PLOT LAYOUT PARAMETERS THAT ARE NOT SET IN THIS SUBROUTINE
C     TAKE THEIR DEFAULT VALUES.
C
      CALL PSETR ('SUBPAGE_LOWER_LEFT_LONGITUDE',-10.0)
      CALL PSETR ('SUBPAGE_LOWER_LEFT_LATITUDE',30.0)
      CALL PSETR ('SUBPAGE_UPPER_RIGHT_LONGITUDE',15.0)
      CALL PSETR ('SUBPAGE_UPPER_RIGHT_LATITUDE',65.0)
C
C     PLOT TITLE (INSTRUCTION STRING IS USED TO PLOT DEGREE SIGN)
C
      CALL PSETC ('TEXT_CHARACTER_2',
     1            'Area definition by Corner Points')
C
      CALL PSETC ('TEXT_CHARACTER_3',
     1            'Map Area is 10W to 15E,'//
     2            '30N to 65N')
C
      CALL PTEXT
C
C     PLOT COASTLINES
C
      CALL PSETI ('MAP_COASTLINE_THICKNESS',3)
      CALL PCOAST
C
      RETURN
      END
      SUBROUTINE PLOT02_THREE
C
C     PLOT DIFFERENT SELECTED AREA IN CYLINDRICAL PROJECTION.
C     COASTLINE RESOLUTION/COLOUR AND GRID INCREMENTS ARE ALSO CHANGED.
C
C     ALL PLOT LAYOUT PARAMETERS THAT ARE NOT SET IN THIS SUBROUTINE
C     TAKE THEIR DEFAULT VALUES.
C
C     SET UP SELECTED AREA
C
      CALL PSETR ('SUBPAGE_LOWER_LEFT_LONGITUDE',100.0)
      CALL PSETR ('SUBPAGE_LOWER_LEFT_LATITUDE',-10.0)
      CALL PSETR ('SUBPAGE_UPPER_RIGHT_LONGITUDE',140.0)
      CALL PSETR ('SUBPAGE_UPPER_RIGHT_LATITUDE',20.0)
C
C     SET UP COASTLINE AND GRID INCREMENTS
C
      CALL PSETR ('MAP_GRID_LATITUDE_INCREMENT',5.0)
      CALL PSETC ('MAP_COASTLINE_RESOLUTION','HIGH')
      CALL PSETI ('MAP_COASTLINE_THICKNESS',2)
      CALL PSETR ('MAP_GRID_LONGITUDE_INCREMENT',5.0)
      CALL PSETC ('MAP_COASTLINE_COLOUR','GREEN')
      CALL PSETC ('MAP_GRID_COLOUR','GREY')
C
C     PLOT COASTLINES
C
      CALL PCOAST
C
C     PLOT TITLE
C
      CALL PSETC ('TEXT_MODE','TITLE')
      CALL PSETC ('TEXT_BOX_BLANKING','OFF')
      CALL PSETC ('TEXT_BORDER','OFF')
      CALL PSETI ('TEXT_FIRST_LINE',1)
      CALL PSETI ('TEXT_LINE_COUNT',3)
      CALL PSETC ('TEXT_CHARACTER_2',
     1            'Coastline Resolution is HIGH and '//
     2            'Grid Intervals are 5')
C
      CALL PSETC ('TEXT_CHARACTER_3',
     1            'Area Definition is by UPPER_RIGHT and '//
     2            'LOWER_LEFT Corners')
C
      CALL PTEXT
C
C     RESET CERTAIN MAP PARAMETERS
C
      CALL PRESET('MAP_COASTLINE_RESOLUTION')
      CALL PRESET('MAP_GRID_LATITUDE_INCREMENT')
      CALL PRESET('MAP_GRID_LONGITUDE_INCREMENT')
C
      RETURN
      END

      PROGRAM PLOT02
C
C     THIS ROUTINE DEMONSTRATES MAGICS PARAMETER SETTINGS FOR COASTLINES
C     AND PROJECTIONS.  THE ILLUSTRATIONS ARE BASED ON CYLINDRICAL AND
C     POLAR STEREOGRAPHIC PROJECTIONS.  THE MAP AREA DEFINITIONS
C     'CORNERS' AND 'CENTRE' ARE USED.
C
C
C     OPEN MAGICS
C
      CALL POPEN
      CALL PSETC ('PS_DEVICE','ps_a4')
      CALL PSETC ('PS_FILE_NAME','plot02-test.ps')
      CALL PSETR ('SUPER_PAGE_Y_LENGTH',16.0)
      CALL PSETR ('SUPER_PAGE_X_LENGTH',16.0)
      CALL PSETC ('PAGE_ID_LINE_USER_TEXT','Tests/Plot02')
C
C     DEFINE PAGE
C
      CALL PSETR ('PAGE_Y_LENGTH',16.0)
      CALL PSETR ('PAGE_X_LENGTH',16.0)
      CALL PSETR ('SUBPAGE_X_POSITION',1.5)
      CALL PSETR ('SUBPAGE_Y_POSITION',1.0)
      CALL PSETR ('SUBPAGE_Y_LENGTH',13.0)
      CALL PSETR ('SUBPAGE_X_LENGTH',13.0)
C
C     GENERATE TEXT FOR TITLE
C
      CALL PSETC ('TEXT_LINE_1','@TEXT_CHARACTER_1@')
      CALL PSETC ('TEXT_LINE_2','@TEXT_CHARACTER_2@')
      CALL PSETC ('TEXT_LINE_3','@TEXT_CHARACTER_3@')
      CALL PSETI ('TEXT_LINE_COUNT',3)
      CALL PSETR ('TEXT_REFERENCE_CHARACTER_HEIGHT',0.5)
      CALL PSETC ('MAP_COASTLINE_COLOUR','GREEN')
      CALL PSETC ('MAP_GRID_COLOUR','GREY')
C
C     PLOT THREE CYLINDRICAL MAPS
C
c      CALL PLOT02_ONE
c      CALL PNEW ('PAGE')
c      CALL PLOT02_TWO
c      CALL PNEW ('PAGE')
c      CALL PLOT02_THREE
c      CALL PNEW ('PAGE')
C
C     PLOT THREE POLAR STEREOGRAPHIC MAPS ON THE NORTHERN HEMISPHERE
C
C      CALL PLOT02_FOUR
C      CALL PNEW ('PAGE')
C      CALL PLOT02_FIVE
C      CALL PNEW ('PAGE')
C      CALL PLOT02_SIX
C      CALL PNEW ('PAGE')
C
C     PLOT THREE POLAR STEREOGRAPHIC MAPS ON THE SOUTHERN HEMISPHERE
C
C      CALL PLOT02_SEVEN
C      CALL PNEW ('PAGE')
C      CALL PLOT02_EIGHT
C      CALL PNEW('PAGE')
C      CALL PLOT02_NINE
C      CALL PNEW('PAGE')
C      CALL PLOT02_TEN
C      CALL PNEW('PAGE')
C      CALL PLOT02_ELEVEN
C      CALL PNEW('PAGE')
C      CALL PLOT02_TWELVE
C      CALL PNEW('PAGE')
C      CALL PLOT02_THIRTEEN
C      CALL PNEW('PAGE')
C      CALL PLOT02_FOURTEEN
C      CALL PNEW('PAGE')
      CALL PLOT02_FIFTEEN
      CALL PNEW('PAGE')
      CALL PLOT02_SIXTEEN
C
C     CLOSE MAGICS
C
      CALL PCLOSE
C
      END
      SUBROUTINE PLOT02_ONE
C
C     PLOT GLOBAL CYLINDRICAL MAP.
C
